def interpret_career(data: dict) -> dict:
    ilgan = data['birth_info']['ilgan']
    geokguk = data['geokguk']['geokguk']
    sipsung_summary = data['sipsung_summary']
    sinsal_names = [s['name'] for s in data['sinsal']]
    yongsin = data['yongsin']['yongsin']
    strength = data['strength']
    ilgan_data = ILGAN_CAREER.get(ilgan, {})

    dominant = max(sipsung_summary, key=sipsung_summary.get) if sipsung_summary else '비견'
    career_tendency = SIPSUNG_CAREER.get(dominant, '')

    extras = []
    if '역마살' in sinsal_names:
        extras.append(
            '🚀 **역마살**이 있어요! 한 곳에 가만히 앉아있는 직업보다는 '
            '출장, 해외 근무, 영업, 운송처럼 움직이는 일이 훨씬 잘 맞습니다. '
            '활동량이 많을수록 오히려 운이 좋아지는 구조예요.'
        )
    if '문창귀인' in sinsal_names:
        extras.append(
            '📚 **문창귀인**이 있어 시험운과 학업운이 좋습니다. '
            '자격증, 고시, 학위 취득에서 좋은 결과를 얻을 확률이 높아요. '
            '공부와 관련된 투자는 절대 헛되지 않을 겁니다.'
        )
    if '학당귀인' in sinsal_names:
        extras.append(
            '🎓 **학당귀인**이 있어 배움이 곧 재산이 되는 사주예요. '
            '전문 분야를 깊이 파면 파를수록 인정받고, '
            '평생학습이 당신의 커리어를 지켜주는 든든한 무기가 됩니다.'
        )

    # 신강/신약 직업 조언
    if strength['is_strong']:
        strength_career = (
            '신강한 사주라 독립적인 일, 리더 역할, 사업에 유리합니다. '
            '남의 밑에서 일하기보다 자기 주도적으로 움직일 수 있는 환경이 맞아요.'
        )
    else:
        strength_career = (
            '신약한 사주라 좋은 조직, 좋은 상사, 좋은 동료가 중요합니다. '
            '혼자 사업을 시작하기보다 든든한 파트너나 조직의 지원을 받으면 크게 성장해요.'
        )

    yongsin_jobs, yongsin_desc = YONGSIN_CAREER.get(yongsin, ('', ''))

    return {
        'category': '직업/적성',
        'suitable_jobs': ilgan_data.get('jobs', []),
        'ilgan_career_desc': ilgan_data.get('desc', ''),
        'career_tendency': career_tendency,
        'dominant_sipsung': dominant,
        'geokguk_note': f'**{geokguk}**의 특성을 살린 직업이 가장 잘 맞습니다.',
        'strength_career': strength_career,
        'yongsin_jobs': yongsin_jobs,
        'yongsin_desc': yongsin_desc,
        'yongsin': yongsin,
        'extras': extras,
    }
