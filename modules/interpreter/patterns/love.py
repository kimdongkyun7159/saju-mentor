def interpret_love(data: dict) -> dict:
    ilgan = data['birth_info']['ilgan']
    gender = data['birth_info']['gender']
    gender_key = 'male' if gender == '남' else 'female'
    sipsung_summary = data['sipsung_summary']
    sinsal_names = [s['name'] for s in data['sinsal']]

    love_style = ILGAN_LOVE.get(ilgan, '')

    spouse_notes = []
    if gender_key == 'male':
        jae_count = sipsung_summary.get('정재', 0) + sipsung_summary.get('편재', 0)
        if jae_count >= 3:
            spouse_notes.append(
                '💫 재성이 3개 이상이라 이성 인연이 풍부합니다. '
                '여러 인연이 겹칠 수 있어서, 한 사람에게 집중하는 것이 중요해요. '
                '이것저것 흔들리면 오히려 좋은 인연을 놓칠 수 있으니 주의하세요.'
            )
        elif jae_count == 0:
            spouse_notes.append(
                '💫 재성이 보이지 않아 연애가 늦을 수 있지만, 걱정하지 마세요. '
                '대운에서 재성이 올 때 좋은 인연이 찾아옵니다. '
                '때가 되면 자연스럽게 운명처럼 만나게 될 거예요.'
            )
    else:
        gwan_count = sipsung_summary.get('정관', 0) + sipsung_summary.get('편관', 0)
        if gwan_count >= 3:
            spouse_notes.append(
                '💫 관성이 3개 이상이라 남자 인연이 복잡할 수 있어요. '
                '여러 사람이 관심을 보일 수 있지만, 신중하게 선택하는 것이 중요합니다. '
                '내가 진짜 원하는 사람이 누구인지 마음의 목소리에 귀 기울여보세요.'
            )
        elif gwan_count == 0:
            spouse_notes.append(
                '💫 관성이 보이지 않아 연애가 늦을 수 있지만, 그건 더 좋은 인연을 기다리는 것일 수 있어요. '
                '대운에서 관성이 올 때가 연애의 타이밍입니다. '
                '조급해하지 말고, 자신을 가꾸는 시간으로 활용하세요.'
            )

    ilji_sipsung = data['sipsung'].get('day', {}).get('branch_sipsung', '')
    spouse_type = SPOUSE_PERSONALITY.get(ilji_sipsung, '')

    extras = []
    if '도화살' in sinsal_names:
        extras.append(
            '🌸 **도화살**이 있어 타고난 매력의 소유자입니다! '
            '이성에게 자연스럽게 인기가 많고, 사람을 끄는 묘한 분위기가 있어요. '
            '연애 기회는 많지만, 그만큼 불필요한 인연도 생길 수 있으니 '
            '진심인 사람과 아닌 사람을 구분하는 눈이 중요합니다.'
        )
    if '천을귀인' in sinsal_names:
        extras.append(
            '✨ **천을귀인** 덕분에 연애에서도 좋은 사람을 만날 확률이 높아요. '
            '위기 순간에도 관계가 좋은 방향으로 풀리는 운이 있습니다.'
        )

    return {
        'category': '연애/궁합',
        'love_style': love_style,
        'spouse_notes': spouse_notes,
        'spouse_type': spouse_type,
        'ilji_sipsung': ilji_sipsung,
        'extras': extras,
    }
